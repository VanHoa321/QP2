// Khai báo các biến trạng thái
let currentQuestionIndex = 0;
let userAnswers = []; // Mảng lưu trữ đáp án người dùng đã chọn

// Tạo đối tượng âm thanh
const correctSound = new Audio('audio.mp3'); 

// Lấy các phần tử DOM
const questionText = document.getElementById('question-text');
const optionsContainer = document.getElementById('options-container');
const prevBtn = document.getElementById('prev-btn');
const nextBtn = document.getElementById('next-btn');
const jumpInput = document.getElementById('jump-input');
const jumpBtn = document.getElementById('jump-btn');
const questionProgress = document.getElementById('question-progress');
const quizContainer = document.getElementById('quiz-container');
const resultContainer = document.getElementById('result-container');
const scoreDisplay = document.getElementById('score');
const totalQuestionsDisplay = document.getElementById('total-questions');

// Hàm bắt đầu/load câu hỏi
function loadQuestion() {
    // Reset lại giao diện
    optionsContainer.innerHTML = '';
    
    // Xử lý ẩn/hiện nút "Câu trước"
    if (currentQuestionIndex === 0) {
        prevBtn.classList.add('d-none');
    } else {
        prevBtn.classList.remove('d-none');
    }

    // Lấy dữ liệu câu hỏi hiện tại
    const currentQuizData = quizData[currentQuestionIndex];
    const savedAnswer = userAnswers[currentQuestionIndex]; // Kiểm tra xem câu này đã trả lời chưa
    
    // Hiển thị tiến độ và câu hỏi
    questionProgress.innerText = `Câu ${currentQuestionIndex + 1}/${quizData.length}`;
    questionText.innerText = currentQuizData.question;

    // Render các nút đáp án
    currentQuizData.options.forEach(option => {
        const button = document.createElement('button');
        button.innerText = option;
        button.classList.add('btn', 'option-btn');
        
        // Trích xuất chữ cái đầu tiên (A, B, C, D)
        const letter = option.charAt(0);
        button.setAttribute('data-letter', letter);
        
        // Bắt sự kiện click
        button.addEventListener('click', () => selectOption(button, currentQuizData.correctAnswer, letter));
        
        // --- PHỤC HỒI TRẠNG THÁI NẾU CÂU HỎI ĐÃ ĐƯỢC TRẢ LỜI ---
        if (savedAnswer) {
            button.classList.add('disabled'); // Khóa nút
            
            // Nếu đây là đáp án người dùng đã chọn
            if (letter === savedAnswer) {
                if (savedAnswer === currentQuizData.correctAnswer) {
                    button.classList.add('correct'); // Chọn đúng
                } else {
                    button.classList.add('incorrect'); // Chọn sai
                }
            }
            // Bôi xanh đáp án đúng (để người dùng đối chiếu nếu họ chọn sai)
            if (letter === currentQuizData.correctAnswer && savedAnswer !== currentQuizData.correctAnswer) {
                button.classList.add('correct');
            }
        }
        
        optionsContainer.appendChild(button);
    });

    // Xử lý nút "Câu tiếp theo"
    if (savedAnswer) {
        // Nếu đã trả lời rồi thì cho phép đi tiếp
        nextBtn.innerText = currentQuestionIndex === quizData.length - 1 ? "Xem điểm số" : "Tiếp theo";
        nextBtn.classList.remove('d-none');
    } else {
        // Nếu chưa trả lời thì ẩn nút Next đi
        nextBtn.classList.add('d-none');
    }
}

// Hàm xử lý khi người dùng chọn đáp án
function selectOption(selectedButton, correctAnswer, selectedLetter) {
    const allButtons = optionsContainer.querySelectorAll('.option-btn');

    // Lưu lại đáp án của người dùng
    userAnswers[currentQuestionIndex] = selectedLetter;

    // Vô hiệu hóa tất cả các nút
    allButtons.forEach(btn => btn.classList.add('disabled'));

    // Kiểm tra đúng/sai và bôi màu
    if (selectedLetter === correctAnswer) {
        selectedButton.classList.add('correct');
        // Phát âm thanh khi đúng
        correctSound.currentTime = 0; 
        correctSound.play().catch(error => console.log("Lỗi phát âm thanh:", error)); 
    } else {
        selectedButton.classList.add('incorrect');
        // Bôi xanh đáp án đúng
        allButtons.forEach(btn => {
            if (btn.getAttribute('data-letter') === correctAnswer) {
                btn.classList.add('correct');
            }
        });
    }

    // Hiện nút đi tiếp
    nextBtn.innerText = currentQuestionIndex === quizData.length - 1 ? "Xem điểm số" : "Tiếp theo";
    nextBtn.classList.remove('d-none');
}

// Chuyển câu tiếp theo
nextBtn.addEventListener('click', () => {
    if (currentQuestionIndex < quizData.length - 1) {
        currentQuestionIndex++;
        loadQuestion();
    } else {
        showResults();
    }
});

// Quay lại câu trước
prevBtn.addEventListener('click', () => {
    if (currentQuestionIndex > 0) {
        currentQuestionIndex--;
        loadQuestion();
    }
});

// Chức năng Tìm/Nhảy đến câu
jumpBtn.addEventListener('click', () => {
    const questionNum = parseInt(jumpInput.value);
    
    // Kiểm tra tính hợp lệ của số nhập vào
    if (questionNum >= 1 && questionNum <= quizData.length) {
        currentQuestionIndex = questionNum - 1; // Mảng bắt đầu từ 0
        loadQuestion();
        jumpInput.value = ''; // Xóa ô input sau khi chuyển
    } else {
        alert(`Vui lòng nhập số hợp lệ từ 1 đến ${quizData.length}`);
    }
});

// Hàm hiển thị điểm số (Tính điểm dựa trên mảng userAnswers)
function showResults() {
    quizContainer.classList.add('d-none');
    resultContainer.classList.remove('d-none');
    
    let finalScore = 0;
    // Chấm điểm lại toàn bộ những câu đã trả lờif
    userAnswers.forEach((ans, index) => {
        if (ans === quizData[index].correctAnswer) {
            finalScore++;
        }
    });
    
    scoreDisplay.innerText = finalScore;
    totalQuestionsDisplay.innerText = quizData.length;
}

// Khởi chạy bài trắc nghiệm ngay khi load xong
loadQuestion();